        String cpf = jTextField47.getText();
        String rg = jTextField48.getText();
        String endereço = jTextField49.getText();
        String telefone = jTextField50.getText();
        String cargo = jTextField51.getText();
        String funçao = jTextField52.getText();
        
        /**criando objeto usando construtor da classe tecnico */
        tecnico t = new tecnico(nome, cpf, rg, endereço, telefone, cargo, funçao);
                     
