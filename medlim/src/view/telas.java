    private void jButton12ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton12ActionPerformed
       String nome = jTextField1.getText();
       String cpf = jTextField2.getText();
       String rg = jTextField3.getText();
       String endereço = jTextField4.getText();
       String telefone = jTextField5.getText();
       
       /**criando objeto usando construtor classe paciente */
       paciente p = new paciente(nome, cpf, rg, endereço, telefone);
       
       if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField3.getText().isEmpty() || jTextField4.getText().isEmpty() || jTextField5.getText().isEmpty()){
           JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
           jTextField1.setText("");
           jTextField2.setText("");
           jTextField3.setText("");
           jTextField4.setText("");
           jTextField5.setText("");  
        } else{
       
        try {
            /**chamando função consultarExistencia_pac da classe paciente */
            if(p.consultarExistencia_pac(cpf)){
            JOptionPane.showMessageDialog(null, "CPF já existe, não é possível cadastrá-lo novamente!");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField5.setText("");           
            return;
            }
        } catch (IOException ex) {        
        }
      
       try {   
          /**chamando função cadastrar_pac da classe paciente */
          p.cadastrar_pac(nome, cpf, rg, endereço, telefone);
          JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível realizar o cadastro, tente novamente!");
        }
       
       jTextField1.setText("");
       jTextField2.setText("");
       jTextField3.setText("");
       jTextField4.setText("");
       jTextField5.setText("");
        }
    }//GEN-LAST:event_jButton12ActionPerformed

